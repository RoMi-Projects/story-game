"""Generate the game's UI fonts from scratch as crisp bitmap pixel fonts.

The game renders at 320x180 and is upscaled with nearest-neighbour filtering, so
a smooth (antialiased) font turns to mush. This module draws original pixel
fonts into atlases and writes matching BMFont descriptors that Godot imports as
fixed-size FontFiles. Every glyph is one pixel on or off, so it stays razor
sharp at any integer scale.

One font is produced: a 5x7 glyph font used for all UI text.
"""

from PIL import Image

GLYPHS_PER_ROW = 16
WHITE = (255, 255, 255, 255)
TRANSPARENT = (0, 0, 0, 0)


class PixelFont:
    """One bitmap font: its glyph table and its pixel metrics."""

    def __init__(self, name, glyphs, width, height, advance, line_height, baseline):
        self.name = name
        self.glyphs = glyphs
        self.width = width
        self.height = height
        self.advance = advance
        self.line_height = line_height
        self.baseline = baseline

    def characters(self):
        return sorted(self.glyphs.keys(), key=ord)

    def origin(self, index):
        column = index % GLYPHS_PER_ROW
        row = index // GLYPHS_PER_ROW
        return column * self.advance, row * self.line_height

    def atlas_size(self, glyph_count):
        rows = (glyph_count + GLYPHS_PER_ROW - 1) // GLYPHS_PER_ROW
        return GLYPHS_PER_ROW * self.advance, rows * self.line_height


def build_fonts(directory):
    """Write the font's atlas (.png) and descriptor (.fnt) into the assets dir."""
    _write_font(_main_font(), directory)


def _write_font(font, directory):
    characters = font.characters()
    atlas = _draw_atlas(font, characters)
    atlas.save(directory / f"{font.name}.png")
    (directory / f"{font.name}.fnt").write_text(_descriptor(font, characters, atlas.size))
    print(f"wrote assets/{font.name}.png ({atlas.width}x{atlas.height}) and assets/{font.name}.fnt")


def _draw_atlas(font, characters):
    width, height = font.atlas_size(len(characters))
    image = Image.new("RGBA", (width, height), TRANSPARENT)
    pixels = image.load()
    for index, character in enumerate(characters):
        _stamp_glyph(font, pixels, character, font.origin(index))
    return image


def _stamp_glyph(font, pixels, character, origin):
    left, top = origin
    for row, bits in enumerate(font.glyphs[character]):
        for column, bit in enumerate(bits):
            if bit == "1":
                pixels[left + column, top + row] = WHITE


def _descriptor(font, characters, atlas_size):
    width, height = atlas_size
    header = [
        f'info face="{font.name}" size={font.height} bold=0 italic=0 charset=""'
        " unicode=1 stretchH=100 smooth=0 aa=0 padding=0,0,0,0 spacing=0,0 outline=0",
        f"common lineHeight={font.line_height} base={font.baseline} scaleW={width}"
        f" scaleH={height} pages=1 packed=0",
        f'page id=0 file="{font.name}.png"',
        f"chars count={len(characters)}",
    ]
    lines = [_char_line(font, index, ch) for index, ch in enumerate(characters)]
    return "\n".join(header + lines) + "\n"


def _char_line(font, index, character):
    left, top = font.origin(index)
    return (
        f"char id={ord(character)} x={left} y={top} width={font.width}"
        f" height={font.height} xoffset=0 yoffset=0 xadvance={font.advance}"
        " page=0 chnl=15"
    )


def _main_font():
    return PixelFont("font", MAIN_GLYPHS, width=5, height=7, advance=6, line_height=9, baseline=7)


